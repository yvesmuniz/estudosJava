
package com.wp.mballem.jsoup;



public class ReadData {
        private double i;
        private double j;
        private double n;
        private double m;
        private double p;

    public ReadData() {
        
    }

    public double getI() {
        return i;
    }

    public void setI(double i) {
        this.i = i;
    }

    public double getJ() {
        return j;
    }

    public void setJ(double j) {
        this.j = j;
    }

    public double getN() {
        return n;
    }

    public void setN(double n) {
        this.n = n;
    }

    public double getM() {
        return m;
    }

    public void setM(double m) {
        this.m = m;
    }

    public double getP() {
        return p;
    }

    public void setP(double p) {
        this.p = p;
    }

        
}
